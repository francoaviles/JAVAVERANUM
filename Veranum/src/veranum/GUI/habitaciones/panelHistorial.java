     private void leerTodos(boolean filtro){
        ArrayList dato;
        if(!filtro)
            dato = DAOHistorialPrecios.sqlLeerTodos();
        else
            dato = DAOHistorialPrecios.sqlByHotel(((ClHoteles)cbHotel.getSelectedItem()).getIdHotel());
        
        dt =  (DefaultTableModel) grDatos.getModel();        
        for (int i = dt.getRowCount() -1; i >= 0; i--){  
            dt.removeRow(i);
        }     
        for(int x=0; x < dato.size(); x++){
            ClHistorialPrecios xx = (ClHistorialPrecios)dato.get(x);
            Object[] fila = new Object[6];
            fila[0] = xx.getIdHistorialPrecios();
            fila[1] = xx.getHotel();
            fila[2] = xx.getUbicacion();
            fila[3] = xx.getPrecio();
            fila[4] = xx.getNuevo_precio();
            fila[5] = xx.getFecha();
            dt.addRow(fila);
        }
    }
     
     private void cargarHotel(){
        for (Object dato : DAOHoteles.sqlLeerTodos()) {
            cbHotel.addItem(dato);
        }
    }

